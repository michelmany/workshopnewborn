<p><strong>1. O que esta política de privacidade cobre</strong></p>
<p>

Esta política cobre como Retratum ("Retratum", "nós", "nos" ou "nosso") trata as informações pessoais que coleta e recebe em www.retratum.com. Nós não repassamos informações sobre você cliente a terceiros: nós usamos a informação compartilhada conosco para o único propósito de executar e melhorar nossos Serviços. Com isso visamos comunicar sobre as informações que recolhemos, os passos que tomamos para proteger e protegê-lo, como nós usamos e compartilhamos informações e, finalmente, como você pode entrar em contato conosco com dúvidas ou preocupações. </p>



<p><strong>2. Informações que coletamos</strong></p>
<p>
O Retratum coleta informações pessoais quando você se cadastra conosco, usa produtos, Serviços ou conteúdos de post em um website Retratum. Esta informação é limitada ao seu nome, endereço de e-mail, nome de usuário, e qualquer informação que você postar em seu site. Sua senha é armazenada de forma segura através de um algoritmo de hash, impedindo a possibilidade de engenharia reversa. </p>

<p>O Retratum também pode coletar informações pessoais se você entra em promoções ou concursos.</p>

<p>O Retratum pode receber automaticamente e gravar informações em nossos servidores a partir de seu navegador, incluindo seu endereço IP, cookies e as páginas que você pedir.</p>

<p>Quanto ao seu conteúdo, você concede-nos o direito de coletar seu material para reproduzir, exibir, executar, adaptar, modificar e distribuir, a exclusivo critério do Retratum.</p>

<p>O Retratum utiliza as informações para os seguintes propósitos gerais: produtos e prestação de Serviços, a identificação e autenticação, melhoria de Serviços, suporte, contato, investigação, denúncias anônimas e divulgação.</p>



<p><strong>3. Endereço de IP</strong></p>
<p>
O Retratum pode, ocasionalmente, obter endereços IP de usuários, dependendo de como você acessa nossos sites. Endereços IP, navegador e sessão de informação podem ser utilizados para vários fins, incluindo, mas não limitados a:</p>

<p>Diagnosticar e prevenir problemas de tecnologia relatados por nossos usuários ou engenheiros que estão associados com os endereços IP controlados por uma empresa de web específica ou provedor de serviços ou ISP.</p>

<p>Estimar o número total de usuários que visitam sites Retratum de regiões geográficas específicas.</p>

<p>Ajudar a determinar quais usuários têm privilégios de acesso a determinados conteúdos, Serviços ou recursos que oferecemos.</p>

<p>Monitorar e prevenir fraude ou abuso. </p>

<p><strong>4. Cookies</strong></p>

<p>Um cookie é uma pequena quantidade de dados, que muitas vezes inclui um identificador anônimo único, que é enviado para o seu navegador a partir de computadores de um site da web e armazenado no disco rígido do seu computador.</p>

<p>Se você rejeitar todos os cookies, você pode não ser capaz de utilizar os Serviços do Retratum ou produtos que exigem login, você pode não ser capaz de tirar o máximo proveito de todas as ofertas.</p>

<p>Você pode configurar seu navegador para aceitar todos os cookies, rejeitar todos os cookies ou notificá-lo quando um cookie é definido.</p>

<p>O Retratum utiliza seus próprios cookies para vários propósitos, incluindo:</p>

<p>Manter o controle das preferências que especificar enquanto você estiver usando nossos produtos e Serviços.</p>

<p>Estimar e relatar nosso tamanho total do público e do tráfego.</p>

<p>Realizar pesquisas para melhorar o conteúdo e os serviços prestados pelo Retratum.</p>

